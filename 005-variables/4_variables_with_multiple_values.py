# assigning multiple values to multiple variables, correspondingly
var1, var2, var3, var4 = 10, "I am string", 5.75, True
print(var1)
print(var2)
print(var3)
print(var4)


# assigning multiple variables to the same value
a = b = c = "Same value"
print(a)
print(b)
print(c)


# unpack list to multiple variables
myList = [1, "Test", 5.75]
v1, v2, v3 = myList
print(v1)
print(v2)
print(v3)
