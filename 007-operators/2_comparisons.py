x = 10
y = 5

print("x = " + str(x))  # + is concatination and not addition, will be explained later
print("y = " + str(y))


# equal
print("x == y = " + str(x == y))

# not equal
print("x != y = " + str(x != y))

# greater than
print("x > y = " + str(x > y))

# greater than or equal
print("x >= y = " + str(x >= y))

# less than
print("x < y = " + str(x < y))

# less than or equal
print("x <= y = " + str(x <= y))