strSample = "This is a sample string"
print(strSample)


# slicing example 1
print(strSample[10:16])


# slicing example 2 (from begining of string)
print(strSample[:16])


# slicing example 2 (to the end of the string)
print(strSample[10:])


# negative indexing
print(strSample[-1])
print(strSample[-13:-7])