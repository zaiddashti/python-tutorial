# The + here is used as concatenation and not addition

# string concatenation
str1 = "This is part"
str2 = "of the string."
result = str1 + str2
print(result)


# Another example with " "
result = str1 + " " + str2
print(result)


# Another example with using method
result = str1.upper() + " " + str2
print(result)
